    /**
     * Permite comprobar el codigo y contraseña del usuario que iniciara sesion.
     * @param xCodigo   el codigo ingresado por el usuario.
     * @param xContrasena la contraseña ingresada por el usuario.
     * @return usuarioContrasenaCorrecto true si se comprueban los datos.
     * @throws Exception Excepcion en el ResultSet rsDatos.
     */
    private boolean ComprobarUsuarioContrasena(String xCodigo, String xContrasena) throws Exception {
                
        ResultSet rsDatos;
        clsUsuarioDAO usuarioDAO = new clsUsuarioDAO();        
        conexion = clsConexionDS.AbrirConexion();
        boolean usuarioContrasenaCorrecto = false;
        
        try
        {
            rsDatos = usuarioDAO.SeleccionarUsuarioCodigo(xCodigo, conexion);            
            while(rsDatos.next())
            {
                if(xContrasena.equals(rsDatos.getString("usuario_contrasena")))
                {
                   usuarioContrasenaCorrecto = true;
                   usuarioNombre = rsDatos.getString("usuario_nombre");
                   usuarioApellido = rsDatos.getString("usuario_apellido");
                   usuarioJefeProyecto = rsDatos.getString("usuario_jefe_proyecto");
                   usuarioId = rsDatos.getString("usuario_id");
                }
                else
                {
                    usuarioContrasenaCorrecto = false;
                    JOptionPane.showMessageDialog(this, "Contraseña Incorrecta", 
                                        "Error autenticando", 0);
                }
            }
            clsConexionDS.CerrarConexion(conexion);
        }
        catch(Exception ex)
        {
            throw ex;
        }
        return usuarioContrasenaCorrecto;
    }
    
    /**
     * Abre el formulario Principal {@link frmPrincipal} donde se trabajara
     * con las ventanas modales del sistema.
     * <p>
     * Se visualiza el formulario, se centra y se maximiza.
     * @param xJefeProyecto variable que ira al formulario Principal {@link frmPrincipal}
     * @param xUsuario variable que ira al formulario Principal {@link frmPrincipal}
     * para que todo el sistema identifique si el usuario es jefe de proyecto o no.
     */
    private void AbrirVentanaPrincipal 
        (
            boolean xJefeProyecto,
            String xUsuario, 
            String xUsuarioId
        ) {
        
        vistacontrol.frmPrincipal obj= new vistacontrol.frmPrincipal(
                xJefeProyecto, xUsuario, xUsuarioId
                );  
        obj.setVisible(true);
        obj.setLocationRelativeTo(null);
        obj.setExtendedState(frmPrincipal.MAXIMIZED_BOTH);        
        
    }
    
     
  
