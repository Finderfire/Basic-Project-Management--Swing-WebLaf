    private void btnIngresarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnIngresarActionPerformed
        
        //declaracion de variables
        String codigo;
        String contrasena;
        
        //toma de datos
        codigo = txtCodigo.getText();
        char[] aux = txtContrasena.getPassword();
        contrasena = new String(aux);
        
        try 
        {
            //Comprueba el usuario y la contraseña
            if(ComprobarUsuarioContrasena(codigo,contrasena))
            {
                JOptionPane.showMessageDialog(this, "Bienvenido(a):\n"+
                        usuarioApellido+", "+usuarioNombre,
                        "Acceso correcto =)",JOptionPane.INFORMATION_MESSAGE);
                         
                if(usuarioJefeProyecto.equals("1"))
                {
                    AbrirVentanaPrincipal(true, usuarioApellido+", "+usuarioNombre, usuarioId);
                }
                else
                {
                    AbrirVentanaPrincipal(false, usuarioApellido+", "+usuarioNombre, usuarioId);
                }                
                this.dispose();                
            }
        } catch (Exception ex) {
            Logger.getLogger(frmInicioSesion.class.getName()).log(Level.SEVERE, null, ex);
        }
        
    }//GEN-LAST:event_btnIngresarActionPerformed

