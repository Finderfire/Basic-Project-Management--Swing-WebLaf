    private void btnCrearProyectoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCrearProyectoActionPerformed
        
        //Declaraciones
        String proyectoCreadoId = "";
        String fechaActual = clsFechas.ObtenerFechaActual();        
        String entregableId;
        
        clsProyectoDTO proyectoDTO = new clsProyectoDTO();
        clsProyectoDAO proyectoDAO = new clsProyectoDAO();
        clsProyectoUsuarioDTO proyectoUsuarioDTO;
        clsProyectoUsuarioDAO proyectoUsuarioDAO = new clsProyectoUsuarioDAO();
        clsProyectoEntregableDTO proyectoEntregableDTO;
        clsProyectoEntregableDAO proyectoEntregableDAO = new clsProyectoEntregableDAO();
        Connection conexion;
        
        JLabel lblMensaje;
        
        try
        {
            conexion = clsConexionDS.AbrirConexion();
            //INSERT PROYECTO            
            proyectoDTO.setProyecto_nombre(txtProyectoNombre.getText());
            proyectoDTO.setProyecto_descripcion(txtProyectoDescripcion.getText());
            proyectoDTO.setProyecto_fecha_creacion(clsFechas.ObtenerFechaActual());

            //metodo
            proyectoDAO.CrearProyecto(proyectoDTO, conexion);

            //OBTENER ID DEL PROYECTO QUE SE CREO
            try 
            {
                proyectoCreadoId = clsFuncionesBD.ObtenerUltimoIdInsertado(conexion);
            } 
            catch (Exception ex) 
            {
                Logger.getLogger(frmNuevoProyecto.class.getName()).log(Level.SEVERE, null, ex);
            }

            //INSERT EQUIPO PROYECTO
            for(String id : arrayProyectoUsuarioId)
            {
                proyectoUsuarioDTO = new clsProyectoUsuarioDTO
                (
                    proyectoCreadoId,
                    id,
                    "",
                    "",
                    "",
                    ""
                );

                //metodo
                proyectoUsuarioDAO.CrearProyectoUsuario(proyectoUsuarioDTO, conexion);

            }

            //INSERT ENTREGABLES
            java.util.List paths = treEntregable.getCheckedNodes(false);
            for (Iterator it = paths.iterator(); it.hasNext();) 
            {
                String[] auxPartes = it.next().toString().split("-");
                entregableId = auxPartes[0].trim();
                
                if(auxPartes.length > 1)
                {
                    proyectoEntregableDTO = new clsProyectoEntregableDTO (
                                            "",
                                            proyectoCreadoId,
                                            entregableId,
                                            fechaActual,
                                            GOOGLE_DOCS_DEFAULT_URL    
                                        );

                    //metodo
                    proyectoEntregableDAO.CrearProyectoEntregable(proyectoEntregableDTO, conexion);
                    //System.out.println("Checked: "+ it.next());   
                }                         
            }  
            clsConexionDS.CerrarConexion(conexion);
            
            lblMensaje = new JLabel("Se creó el proyecto correctamente");
            lblMensaje.setFont(FUENTE_DROID);
            JOptionPane.showMessageDialog(this, lblMensaje, 
                    "Creación correcta", JOptionPane.INFORMATION_MESSAGE);
            objPrincipal.AbrirModalProyectos();
        }
        catch (HeadlessException ex)
        {
            
            Logger.getLogger(frmNuevoProyecto.class.getName()).log(Level.SEVERE, null, ex);
                            
        }
        
        
    }//GEN-LAST:event_btnCrearProyectoActionPerformed

