    private void CargarArbolMetodologia() {
        
        clsMetodologiaDAO metodologiaDAO = new clsMetodologiaDAO();
        Connection conexion;
        Object[][] datosEtapa;
        Object[][] datosEntregable;
        String auxMetodologiaId;        
        
        conexion = clsConexionDS.AbrirConexion();
        auxMetodologiaId = arrayMetodologiaId.get(lstMetodologia.getSelectedIndex());
        datosEtapa =  metodologiaDAO.SeleccionarEtapaEntregableMetodologia (
                    "etapa", 
                    " etapa_id, etapa_nombre", 
                    " metodologia_id='" + auxMetodologiaId + "' ", 
                    conexion
                    );
        
        if( datosEtapa.length > 0)
        {
            for (int i=0; i < datosEtapa.length; i++) 
            {
                //se crea nodo Etapa
                dmtnEtapa = new DefaultMutableTreeNode(datosEtapa[i][1]);
                dmtnMetodologia.add(dmtnEtapa);
                //se obtiene los Entregable
                datosEntregable = metodologiaDAO.SeleccionarEtapaEntregableMetodologia (
                                "entregable", 
                                "entregable_id,entregable_nombre", 
                                " etapa_id='" + datosEtapa[i][0].toString() + "' ", 
                                conexion
                                );
                
                if( datosEntregable.length > 0)
                {
                    for (int j=0; j< datosEntregable.length; j++) 
                    {
                        //intento de colocar en blanco
                        JLabel l = new JLabel((String) datosEntregable[j][0]);     
                        l.setForeground(Color.white);
                        //fin del intento
                        dmtnEntregable = new DefaultMutableTreeNode(l.getText() +
                                " - " + (String) datosEntregable[j][1] );
                        dmtnEtapa.add(dmtnEntregable);
                        
                    }
                }
            }
        }
        clsConexionDS.CerrarConexion(conexion);
        DefaultTreeModel modelo = new DefaultTreeModel(dmtnMetodologia);
        treEntregable.setModel(modelo);
        
    }
    
    
    /**
     *  Carga el JList de metodologias disponibles en la BD.
     *  <p>
     *  Almacena en arrayMetodologiaDescripcion y arrayMetodologiaId
     *  los respectivos datos obtenidos en la BD.
     */
    private void CargarListaMetodologia() {
        
        DefaultListModel dlmMetodologia = new DefaultListModel();
        clsMetodologiaDAO metodologiaDAO = new clsMetodologiaDAO();
        ArrayList<clsMetodologiaDTO> arrayMetodologia;
        String fila[];
        Connection conexion;
        
        dlmMetodologia.removeAllElements();
        fila = new String [3];
        conexion = clsConexionDS.AbrirConexion();
        arrayMetodologia = metodologiaDAO.SeleccionarMetodologia(conexion);
        for(clsMetodologiaDTO m : arrayMetodologia)
        {
            fila[0] = m.getMetodologia_nombre();
            fila[1] = m.getMetodologia_descripcion();
            fila[2] = m.getMetodologia_id();
            dlmMetodologia.addElement(fila[0]);
            arrayMetodologiaDescripcion.add(fila[1]);
            arrayMetodologiaId.add(fila[2]);
        }
        lstMetodologia.setModel(dlmMetodologia);
        clsConexionDS.CerrarConexion(conexion);
    }
    
    /**
     * Carga el JList de usuarios disponibles e la BD con los apellidos
     * y nombres.
     * <p>
     * Almacena el usuario_id en un arrayList.
     */
    private void CargarListaUsuario() {
        
        clsUsuarioDAO usuarioDAO = new clsUsuarioDAO();
        ArrayList<clsUsuarioDTO> arrayUsuario;
        String fila[];
        Connection conexion;
        
        dlmUsuario.removeAllElements();
        fila = new String [3];
        conexion = clsConexionDS.AbrirConexion();
        arrayUsuario = usuarioDAO.SeleccionarUsuario(conexion);
        for(clsUsuarioDTO u : arrayUsuario) 
        {
            fila[0] = u.getUsuario_apellido() + ", " + u.getUsuario_nombre();
            fila[1] = u.getUsuario_id();
            fila[2] = u.getUsuario_jefe_proyecto();            
            if( !fila[0].equals(frmPrincipal.usuarioApellidoNombre) )
            {                
                dlmUsuario.addElement(fila[0]);
                arrayUsuarioId.add(fila[1]);                
            }
        }
        lstUsuario.setModel(dlmUsuario);
        clsConexionDS.CerrarConexion(conexion);
        
    }
    
